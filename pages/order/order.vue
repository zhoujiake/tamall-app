<script>
	import uniLoadMore from '@/components/uni-load-more/uni-load-more.vue';
	import empty from "@/components/empty";
	import Json from '@/Json';
	import {mapState } from 'vuex';  
	export default {
		components: {
			uniLoadMore,
			empty
		},
		data() {
			return {
				pageNum: 1,
				tabCurrentIndex: 0,
				navList: [{
						state: 0,
						text: '',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 1,
						text: '',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 2,
						text: '',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 3,
						text: '',
						loadingType: 'more',
						orderList: []
					},
					{
						state: 4,
						text: '',
						loadingType: 'more',
						orderList: []
					}
				],
				texts : []
			};
		},
		
		onLoad(options) {
			/**
			 * 修复app端点击除全部订单外的按钮进入时不加载数据的问题
			 * 替换onLoad下代码即可
			 */
			this.tabCurrentIndex = + options.state;
			// #ifndef MP
			this.loadData()
			// #endif
			// #ifdef MP
			if(options.state == 0){
				this.loadData()
			}
			// #endif
			uni.setNavigationBarTitle({
				title: this.lang.myOrders
			})
			this.texts.push(this.lang.allOrders)
			this.texts.push(this.lang.pendingPayment)
			this.texts.push(this.lang.toBeReceived)
			this.texts.push(this.lang.receipt)
			this.texts.push(this.lang.afterSale)
			uni.startPullDownRefresh();
		},
		computed: {
			...mapState(['lang','userLang'])						
		},
		// 下拉刷新
		onPullDownRefresh(){
			let index = this.tabCurrentIndex;
			let navItem = this.navList[index];
			navItem.orderList = []
			navItem.loaded = false
			this.pageNum = 1
			this.loadData('tabChange');
		},
		methods: {
			//获取订单列表
			async loadData(source) {
				//这里是将订单挂载到tab列表下
				let index = this.tabCurrentIndex;
				let navItem = this.navList[index];
				let state = navItem.state;
				if(source === 'tabChange' && navItem.orderList.length > 0) {
					this.pageNum += 1
					//tab切换只有第一次需要加载数据
					return;
				}
				if(source === 'tabChange' && navItem.loaded === true) {
					//tab切换只有第一次需要加载数据
					return;
				}
				if(navItem.loadingType === 'loading') {
					//防止重复加载
					return;
				}
				navItem.loadingType = 'loading';
				var url = '';
				if (this.tabCurrentIndex === 1) { // 待付款
					url = this.$baseUrl + "ordersForApp?page=" + this.pageNum + "&orderStatus=" + 0;
				} else if (this.tabCurrentIndex === 2) { // 待收货/已配货/已出库
					url = this.$baseUrl + "ordersForApp?page=" + this.pageNum + "&orderStatus=" + 1;
				} else if (this.tabCurrentIndex === 3) { // 待收货
					url = this.$baseUrl + "ordersForApp?page=" + this.pageNum + "&orderStatus=" + 4;
				}  else if (this.tabCurrentIndex === 4) { // 售后
					url = this.$baseUrl + "ordersForApp?page=" + this.pageNum + "&orderStatus=" + 4;
				} else { // 全部
					url = this.$baseUrl + "ordersForApp?page=" + this.pageNum;
				}				// 调用我的订单接口（全部）
				uni.request({
					url: url,
					method: "GET",
					success: (res) => {
						if (res.data.resultCode == 200) {
							if (res.data.data.list.length) {
								res.data.data.list.forEach(item=>{
									navItem.orderList.push(item);
								})
							}
							this.pageNum += 1
							//loaded新字段用于表示数据加载完毕，如果为空可以显示空白页
							this.$set(navItem, 'loaded', true);
							//判断是否还有数据， 有改为 more， 没有改为noMore 
							navItem.loadingType = 'more';
						} else {
							//loaded新字段用于表示数据加载完毕，如果为空可以显示空白页
							this.$set(navItem, 'loaded', false);
							//判断是否还有数据， 有改为 more， 没有改为noMore 
							navItem.loadingType = '';
						}
						uni.stopPullDownRefresh();
					}
				});
			}, 
			parsingDate(dateStr) {
				return this.$formatDate.dateFormat(dateStr);
		    },
			//swiper 切换
			changeTab(e) {
				this.tabCurrentIndex = e.target.current;
				this.pageNum = 1
				this.loadData('tabChange');
			},
			//顶部tab点击
			tabClick(index){
				this.tabCurrentIndex = index;
			},
			//删除订单
			deleteOrder(item, index){
				uni.showLoading({
					title: '请稍后'
				})
				// 取消订单
				uni.request({
					url: this.$baseUrl + "orders/"+ item.orderId +"/delete",
					method: "PUT",
					success: (res) => {
						if (res.data.resultCode == 200) {
							//取消订单后删除待付款中该项
							this.navList[this.tabCurrentIndex].orderList.splice(index, 1);
						} else { 
							
						}
						uni.hideLoading();
					}
				});
			},
			//支付
			pay(item) {
				let orderNo = item.orderNo
				let amount = item.totalPrice
				uni.redirectTo({
					url: `/pages/money/pay?orderNo=${orderNo}&amount=${amount}`
				})
			},
			//取消订单
			cancelOrder(item){
				uni.showLoading({
					title: '请稍后'
				})
				// 取消订单
				uni.request({
					url: this.$baseUrl + "orders/"+ item.orderNo +"/cancel",
					method: "PUT",
					success: (res) => {
						if (res.data.resultCode == 200) {
							//取消订单后删除待付款中该项
							item.orderStatus = -1
						} else {
							
						}
						uni.hideLoading();
					}
				});
			},
			// 查看物流
			showLogistics(item) {
				uni.navigateTo({
					url: `/pages/order/orderStep?orderId=${item.orderId}&userAddress=${item.userAddress}`
				});
			},
			// 确认收货
			confirmReceipt(item) {
				uni.showModal({
					confirmText: this.lang.ok,
					cancelText: this.lang.no,
					content: this.lang.confirmReceiptMsg,
					success: (e)=>{
						if(e.confirm){
							// 取消订单
							uni.request({
								url: this.$baseUrl + "orders/"+ item.orderNo +"/finish",
								method: "PUT",
								success: (res) => {
									if (res.data.resultCode == 200) {
										//取消订单后删除待付款中该项
										item.orderStatus =+1
										item.orderStatusString = this.getOrderStateTransf("交易成功")
									} else {
										
									}
									uni.hideLoading();
								}
							});
						}
					}
				})
			},
			//订单状态文字和颜色
			orderStateExp(state) {
				let stateTip = '',
					stateTipColor = '#fa436a';
				switch(+state){
					case 1:
						stateTip = '待付款'; break;
					case 2:
						stateTip = '待发货'; break;
					case 9:
						stateTip = '订单已关闭'; 
						stateTipColor = '#909399';
						break;
					//更多自定义
				}
				return {stateTip, stateTipColor};
			},
			getOrderStateTransf(text) {
				let trans = "";
				if(text.includes('待支付')) {
				   	trans = this.lang.toBePaid;
					return trans
				}
				if(text.includes('已支付')) {
				   	trans = this.lang.paid;
					return trans
				}
				if(text.includes('配货完成')) {
				   	trans = this.lang.distribution;
					return trans
				}
				if(text.includes('出库成功')) {
				   	trans = this.lang.shipped;
					return trans
				}
				if(text.includes('交易成功')) {
				   	trans = this.lang.afterSaleInOrder;
					return trans
				}
			},
			// 显示图片
			showPic(index, item) {
				let urls = []
				item.newBeeMallOrderItemVOS.forEach(item => {
					urls.push(item.goodsCoverImg)
				})
				uni.previewImage({
					indicator:"none",
					current:index,
					urls: urls
				});
			},
			// 分类名称语言切换
			switchLanguageForString(str, index){
				if(str.indexOf("|") == -1) {
					return str
				} else {
					var arr = str.split('|');
					return arr[index];
				}
			},
		},
	}
</script>

