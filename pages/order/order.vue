				@click="tabClick(index)"
			>
				{{texts[index]}}
			</view>
		</view>

		<swiper :current="tabCurrentIndex" class="swiper-box" duration="300" @change="changeTab">
			<swiper-item class="tab-content" v-for="(tabItem,tabIndex) in navList" :key="tabIndex">
				<scroll-view 
					class="list-scroll-content"
					scroll-y
					@scrolltolower="loadData"
				>
					<!-- 空白页 -->
					<empty v-if="tabItem.loaded === true && tabItem.orderList.length === 0"></empty>
					
					<!-- 订单列表 -->
					<view 
						v-for="(item, index) in tabItem.orderList" :key="index"
						class="order-item">
						
						<view class="i-top b-b">
							<text class="time">{{parsingDate(item.createTime)}}</text>
							<text class="state" :style="{color: item.stateTipColor}">
										{{getOrderStateTransf(item.orderStatusString)}}</text>
							<text
								v-if="item.orderStatus === 0"
								class="del-btn yticon icon-iconfontshanchu1"
								@click="deleteOrder(item, index)"
							></text>
						</view>
						
						<scroll-view v-if="item.newBeeMallOrderItemVOS.length > 1" class="goods-box" scroll-x>
							<view
								v-for="(goodsItem, goodsIndex) in item.newBeeMallOrderItemVOS" :key="goodsIndex"
								class="goods-item">
								<image class="goods-img" :src="goodsItem.goodsCoverImg" @tap="showPic(goodsIndex, item)"
								mode="aspectFill"></image>
							</view>
						</scroll-view>
						<view 
							v-if="item.newBeeMallOrderItemVOS.length === 1" 
							class="goods-box-single"
							v-for="(goodsItem, goodsIndex) in item.newBeeMallOrderItemVOS" :key="goodsIndex">
							
							<image class="goods-img" :src="goodsItem.goodsCoverImg" @tap="showPic(goodsIndex, item)"
							mode="aspectFill"></image>
							<view class="right">
								
								<text v-if="userLang === 'tb'" class="title clamp">
									{{switchLanguageForString(goodsItem.goodsName, 0)}}
								</text>
								<text v-else class="title clamp">
									{{switchLanguageForString(goodsItem.goodsName, 1)}}
								</text>
								
								<text class="attr-box">{{goodsItem.goodsCount}}</text>
								<text class="price">{{lang.moneyFlag + goodsItem.sellingPrice}}</text>
							</view>
						</view>
						
						<view class="price-box">
							{{lang.count}}
							<text class="num">{{item.newBeeMallOrderItemVOS.length}}</text>
							<!-- {{lang.orderItemsCount + lang.discountedPrice}} -->
							<text class="price">{{lang.payTotoal + " "+item.totalPrice}}</text>
						</view>
						
						<view class="action-box b-t" v-if="item.orderStatus == 0">
							<button class="action-btn" @click="cancelOrder(item)">{{lang.cancelOrder}}</button>
							<button class="action-btn recom" @click="pay(item)">{{lang.payment}}</button>
						</view>
						
						<view class="action-box b-t" v-if="item.orderStatus == 3">
							<button class="action-btn" @click="confirmReceipt(item)">{{lang.confirmReceipt}}</button>
							<button class="action-btn" @click="showLogistics(item)">{{lang.showLogist}}</button>
